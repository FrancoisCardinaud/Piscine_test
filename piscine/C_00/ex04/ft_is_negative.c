#include <unistd.h>

void	ft_is_negative(int i)
{
	char	neg;
	char	pos;

	neg = 'N';
	pos = 'P';
	if (i < 0)
	{
		write(1, &neg, 1);
	}	
	else
	{
		write(1, &pos, 1);
	}
}
