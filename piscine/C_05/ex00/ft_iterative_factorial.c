int	ft_iterative_factorial(int nb)
{
	int	i;
	int	r;

	if ((nb < 0) || (nb > 12))
	{
		return (0);
	}
	if ((nb >= 0) && (nb < 1))
	{
		return (1);
	}
	else
	{
		i = nb;
		r = 1;
		while (i > 1)
		{
			r = r * (i);
			i--;
		}
		return (r);
	}
}
